function sortByColumn(a, colIndex, reverse) {
  if (reverse == true) {
    a.sort(sortFunction).reverse();
  } else {
    a.sort(sortFunction);
  }

  function sortFunction(a, b) {
    if (a[colIndex] === b[colIndex]) {
      return 0;
    } else {
      return a[colIndex] < b[colIndex] ? -1 : 1;
    }
  }
  return a;
}

